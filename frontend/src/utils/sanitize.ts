import DOMPurify, { type Config } from 'dompurify'

const PURIFY_CONFIG: Config = {
  ALLOWED_TAGS: [
    'h1',
    'h2',
    'h3',
    'h4',
    'h5',
    'h6',
    'p',
    'a',
    'ul',
    'ol',
    'li',
    'code',
    'pre',
    'blockquote',
    'em',
    'strong',
    'table',
    'thead',
    'tbody',
    'tr',
    'th',
    'td',
    'br',
    'hr',
    'img',
    'span',
    'div',
    'mark',
    'sup',
    'sub',
    'dl',
    'dt',
    'dd',
    'figure',
    'figcaption',
    'details',
    'summary',
    'abbr',
  ],
  ALLOWED_ATTR: [
    'href',
    'src',
    'alt',
    'title',
    'class',
    'id',
    'colspan',
    'rowspan',
    'width',
    'height',
    'target',
    'rel',
  ],
  ALLOW_DATA_ATTR: false,
}

export function sanitizeHTML(dirty: string): string {
  return DOMPurify.sanitize(dirty, PURIFY_CONFIG) as string
}
